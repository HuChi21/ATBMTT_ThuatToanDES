    private void fileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fileButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter textFilter = new FileNameExtensionFilter("Text","txt","docx");
        fileChooser.setFileFilter(textFilter);
        fileChooser.setMultiSelectionEnabled(false);
        fileChooser.setCurrentDirectory(new File("C:\\Users\\HuChi\\Desktop"));
        int x = fileChooser.showDialog(this,"Select");
        if (x==JFileChooser.APPROVE_OPTION){
            File f = fileChooser.getSelectedFile();
            
            fileLabel.setText(f.getName());
            Scanner sc;
            try {
                sc = new Scanner(f);
                jnoidungfile.setText(sc.nextLine());
            } catch (FileNotFoundException ex) {
                Logger.getLogger(From.class.getName()).log(Level.SEVERE, null, ex);
            }           
        }
        
    }//GEN-LAST:event_fileButtonActionPerformed
    
