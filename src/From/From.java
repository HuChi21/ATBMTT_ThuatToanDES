    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            if(KtSNT(Long.parseLong(jTextP.getText()))){
                if(Integer.parseInt(jTextP.getText()) <= Integer.parseInt(jTextM.getText())){
                    JOptionPane.showMessageDialog(this,"Số Thành Viên Dữ Khóa Phải nhỏ Hơn P.","Error",JOptionPane.ERROR_MESSAGE);
               }
               else{
                  if(Integer.parseInt(jTextP.getText())>Integer.parseInt(jTextK.getText())){
                      if(Long.parseLong(jTextT.getText()) <= Long.parseLong(jTextM.getText())){
                         ChiSeKhoa chiase=new ChiSeKhoa();
                         ds= chiase.chikhoa(Long.parseLong(jTextK.getText()), Long.parseLong(jTextP.getText()),
                             Long.parseLong(jTextM.getText()), Long.parseLong(jTextT.getText()));
                         ArrayList<Long> sa=chiase.dA;
                         jTableA.setModel(new dsADAO(sa));
                         jTableK.setModel(new ChiaSeKhoaDAO(ds));
                      }else{
                          JOptionPane.showMessageDialog(this,"số thành viến mở khóa không được lớn hơn số thành viên giữ khóa","Error",JOptionPane.ERROR_MESSAGE);
                      }
                  }
                  else{
                      JOptionPane.showMessageDialog(this,"Khóa Phải nhỏ Hơn P.","Error",JOptionPane.ERROR_MESSAGE);
                  }      
                }
            }else{
                JOptionPane.showMessageDialog(this,"P Không Phải Số Nguyên Tố","Error",JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,"Nhập Sai kiểu dữ liệ(Tất cả đều là số nguyên)");
        }
       
    }//GEN-LAST:event_jButton3ActionPerformed

